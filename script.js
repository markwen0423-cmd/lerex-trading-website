// ==================== 语言切换功能 ====================
let currentLang = localStorage.getItem('selectedLanguage') || 'en';

function applyLanguage(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    
    if (translations[lang] && translations[lang][key]) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translations[lang][key];
      } else {
        el.innerHTML = translations[lang][key];
      }
    }
  });
  
  // 阿拉伯语RTL支持
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.setAttribute('lang', 'ar');
    document.body.classList.add('rtl');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.setAttribute('lang', lang);
    document.body.classList.remove('rtl');
  }
}

// ==================== 文章详情渲染函数 ====================
function renderArticleDetail(articleId) {
  const article = articleContent[articleId];
  if (!article) {
    console.error('Article not found:', articleId);
    return;
  }
  
  const lang = currentLang;
  const content = article[lang];
  if (!content) {
    // 如果当前语言没有内容，回退到英语
    if (article.en) {
      renderArticleDetailWithContent(article.en);
    }
    return;
  }
  
  renderArticleDetailWithContent(content);
}

function renderArticleDetailWithContent(content) {
  const categoryEl = document.getElementById('article-category');
  const dateEl = document.getElementById('article-date');
  const titleEl = document.getElementById('article-title');
  const introEl = document.getElementById('article-intro');
  const contentContainer = document.getElementById('article-content');
  
  if (categoryEl) categoryEl.textContent = content.category;
  if (dateEl) dateEl.textContent = content.date;
  if (titleEl) titleEl.textContent = content.title;
  if (introEl) introEl.textContent = content.intro;
  
  if (contentContainer) {
    contentContainer.innerHTML = '';
    content.sections.forEach(section => {
      const sectionDiv = document.createElement('div');
      sectionDiv.className = 'article-section';
      
      const heading = document.createElement('h2');
      heading.textContent = section.heading;
      sectionDiv.appendChild(heading);
      
      const paragraph = document.createElement('p');
      paragraph.textContent = section.content;
      sectionDiv.appendChild(paragraph);
      
      if (section.list && section.list.length) {
        const ul = document.createElement('ul');
        section.list.forEach(item => {
          const li = document.createElement('li');
          li.textContent = item;
          ul.appendChild(li);
        });
        sectionDiv.appendChild(ul);
      }
      
      contentContainer.appendChild(sectionDiv);
    });
    
    if (content.conclusion) {
      const conclusionDiv = document.createElement('div');
      conclusionDiv.className = 'article-conclusion';
      const p = document.createElement('p');
      p.textContent = content.conclusion;
      conclusionDiv.appendChild(p);
      contentContainer.appendChild(conclusionDiv);
    }
    
    if (content.cta) {
      const ctaDiv = document.createElement('div');
      ctaDiv.className = 'article-cta';
      const p = document.createElement('p');
      p.textContent = content.cta;
      ctaDiv.appendChild(p);
      const ctaLink = document.createElement('a');
      ctaLink.href = 'contact.html';
      ctaLink.className = 'btn btn-primary';
      ctaLink.setAttribute('data-i18n', 'nav_contact');
      ctaLink.textContent = translations[currentLang]?.nav_contact || 'Contact Us';
      ctaDiv.appendChild(ctaLink);
      contentContainer.appendChild(ctaDiv);
    }
  }
  
  document.title = content.title + ' - Lerex Trading';
}

